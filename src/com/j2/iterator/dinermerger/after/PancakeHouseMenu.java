package com.j2.iterator.dinermerger.after;

import java.util.ArrayList;

public class PancakeHouseMenu implements Menu {
    ArrayList menuItems;
 
    public PancakeHouseMenu() {
        menuItems = new ArrayList();
    
        addItem("K&B's Pancake Breakfast", 
                "Pancakes with scrambled eggs, and toast", true, 2.99);
        addItem("Regular Pancake Breakfast", 
                "Pancakes with fried eggs, sausage", false, 2.99);
        addItem("Blueberry Pancakes",
                "Pancakes made with fresh blueberries", true, 3.49);
        addItem("Waffles",
                "Waffles, with your choice of blueberries or strawberries", true, 3.59);
        addItem("채식용 팬케이크 아침식사",
        		"오이와 당근을 곁들인 팬케이크",true,2.59);
        addItem("팬케이크와 초콜릿 딥핑 소스",
        		"팬케이크와 달콤한 초콜릿 팁핑 소스",true,3.39);
        addItem("토스트와 스크램블 에그",
        		"야채만 들어가있는 토스트와 스크램블 에그",true,2.54);
        
    }
    public void addItem(String name, String description, boolean vegetarian, double price) {
        MenuItem menuItem = new MenuItem(name, description, vegetarian, price);
        menuItems.add(menuItem);
    }
    public ArrayList getMenuItems() {
        return menuItems;
    }
    public Iterator createIterator() {
        return new PancakeHouseMenuIterator(menuItems);
    }
    public String toString() {
        return "Objectville Pancake House Menu";
    }
   
}